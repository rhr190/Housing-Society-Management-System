    public static boolean isNumeric(String str) { 
      try {  
        Integer.parseInt(str);
        if(Integer.valueOf(str) <= 0){
            return false;
        }
        return true;
      } catch(NumberFormatException e){  
        return false;  
      }  
    }
    
