    private void fund_calculation() {
        String totalSocietyFee="0";
        String totalDonation="0";
        String totalHallRent="0";
        Connection connection;
        try {
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=HSMS_Project;selectMethod=cursor", "sa", "123456");
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement
                   .executeQuery("SELECT SUM(Amount) as totalAmount FROM FEES");           
            
            if(resultSet.next()){
                totalSocietyFee = resultSet.getString("totalAmount");
                jTextField12.setText(totalSocietyFee);
            }
            resultSet = statement
                   .executeQuery("SELECT SUM(RentFee) as totalAmount FROM HALL_BOOKING_SMI");           
            
            if(resultSet.next()){
                totalHallRent = resultSet.getString("totalAmount");
                if(totalHallRent == null){
                    totalHallRent="0";
                }
                jTextField13.setText(totalHallRent);
            }
            
            resultSet = statement
                   .executeQuery("SELECT SUM(Amount) as totalAmount FROM DONATION");           
            
            if(resultSet.next()){
                totalDonation = resultSet.getString("totalAmount");
                if(totalDonation == null){
                    totalDonation="0";
                }
                jTextField11.setText(totalDonation);
                
                
            }
            int total = Integer.parseInt(totalSocietyFee)+Integer.parseInt(totalDonation)+Integer.parseInt(totalHallRent);
            jTextField14.setText(String.valueOf(total));
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
