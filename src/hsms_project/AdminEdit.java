    public ArrayList<TimeslotClass> rentList(){
        ArrayList<TimeslotClass> timeslotList= new ArrayList<>();
        try{
            Connection connection;
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=HSMS_Project;selectMethod=cursor", "sa", "123456");
            Statement statement = connection.createStatement();
            
            String query;
            query = "select * from HALL_TIME_RENT_SMI";
            
            ResultSet resultset = statement.executeQuery(query);
            TimeslotClass timeslotClass;
            while(resultset.next()){
                timeslotClass = new TimeslotClass(resultset.getString("Timeslots"), resultset.getInt("Rent"));
                timeslotList.add(timeslotClass);
                
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        return timeslotList;
    }
    
