    private void deleteDonationBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDonationBtnActionPerformed
        // TODO add your handling code here:
        String purposename = jTextField1.getText();
        if(purposename.equals("General Welfare")){
            JOptionPane.showMessageDialog(this, "This is the default donation section.\nIt can not be removed.");
            return;
        }
        try{
            Connection connection;
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=HSMS_Project;selectMethod=cursor", "sa", "123456");
            Statement statement = connection.createStatement(); 
            String query;
            query = "Select COUNT(*) as count from DONATION_PURPOSE where PurposeName='"+purposename+"'";
            ResultSet rs = statement.executeQuery(query);
            if(rs.next()){
                if(!rs.getString("count").equals("0")){
                    query = "UPDATE DONATION "
                    + "SET PurposeName='General Welfare' where PurposeName='"+purposename+"'";
                    statement.executeUpdate(query);
                    
                    query = "DELETE from DONATION_PURPOSE "
                    + "where PurposeName='"+purposename+"'";
                    statement.executeUpdate(query);
                    JOptionPane.showMessageDialog(this, "Donation purpose has been removed");
                    show_table_donation_purpose();
                    jComboBox2.removeAllItems();
                    fill_up_combo_donation();
                }else{
                    JOptionPane.showMessageDialog(this, "Invalid purpose name!");
                    return;
                }
            }
            
            //JOptionPane.showMessageDialog(this, "Donation purpose name updated successfully");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_deleteDonationBtnActionPerformed

